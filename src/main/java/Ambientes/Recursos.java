package Ambientes;

public class Recursos {
    private int agua;
    private int luz;
    private int nutrientes;

    public Recursos(int agua, int luz, int nutrientes) {
        this.agua = agua;
        this.luz = luz;
        this.nutrientes = nutrientes;
    }

    // Metodos getters y setters
    public int getAgua() {
        return agua;
    }

    public void setAgua(int agua) {
        this.agua = agua;
    }



}
